package org.accenture.training.day1;

public class ForLoop {

	public static void main(String[] args) {
		int i;int j;
		for ( i = 91;i <= 100; i++) {
			System.out.println("The Iteration : "+i);
			}
		System.out.println("Final value of i is now : "+i);
		
	
	for ( j = 100; j >= 90; j--) {
		System.out.println("The Iteration : "+j);
		}
	System.out.println("Final value of i is now : "+j);
	}

}
