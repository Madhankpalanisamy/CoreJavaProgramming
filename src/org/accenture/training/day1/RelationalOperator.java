package org.accenture.training.day1;

public class RelationalOperator {

	public static void main(String[] args) {
		int data1 = 100;
		int data2 = 200;
		
		System.out.println(data1 + "==" + data2  + "is"  + (data1==data2));
		System.out.println(data1 + "!=" + data2  + "is"  + (data1!=data2));
		System.out.println(data1 + ">" + data2  + "is"  + (data1>data2));
		System.out.println(data1 + "<" + data2  + "is"  + (data1<data2));
		System.out.println(data1 + ">=" + data2  + "is" + (data1>=data2));
		System.out.println(data1 + ">=" + data2  + "is" + (data1<=data2));

	}

}
